# Python - Tuple Methods
print("Python - Tuple Methods:")

# Tuple count() method
print("\nTuple count() method:")
tuple1 = (1, 2, 3, 3, 5, 5, 4, 7, 6, 5, 5, 0, 9)

print(tuple1.count(5))
print(tuple1.count(3))
print(tuple1.count(9))

# Tuple index() method
print("\nTuple index() method:")
tuple2 = ('apple', 'banana', 'cherry', 'apple')

print(tuple2.index('apple'))
print(tuple2.index('banana'))
print(tuple2.index('cherry'))
