# Python - Output Variables

# Output a variable
a = "Hi"
print(a)

# Output multiple variables
txt1 = 'I am'
txt2 = 'learning'
txt3 = 'Python!'
print(txt1, txt2, txt3)

# Output multiple variables using + operator
txt4 = 'Good '
txt5 = 'Luck!'
