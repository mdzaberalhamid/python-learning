# Python - Escape Characters

# use of \ (backslash) in string
str_1 = "I am learning \"Python - Escape Characters\"!"
print(str_1)

# adding a \ (backslash)
str_2 = "Adding one \\ (backslash)"
print(str_2)

# creating a new line in a string
str_3 = "Hello \nWorld!"
print(str_3)

# tab in a string
str_4 = "Hi,\t I am Zaber!"
print(str_4)
