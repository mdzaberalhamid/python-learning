# Python - Change Dictionary Items
print("Python - Change Dictionary Items:")

# Change values
print("\nChange values:")
d1 = {"str": "String 1", "int": 57, "bool": True}
d1["int"] = 90
print(d1)

# Update dictionary
print("\nUpdate dictionary:")
d2 = {"name": "Zaber", "age": 29, "weight": 49}
d2.update({"weight": 50})
print(d2)
