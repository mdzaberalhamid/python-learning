# Python Variables - Assign Multiple Values

# Assigning many values to multiple variables
a, b, c = 'Mango', 'Banana', 'Apple'
print(a)
print(b)
print(c)
# print(a, b, c)  # Try this also

# Assigning one value to multiple variables
d = e = f = 3
print(d)
print(e)
print(f)

# Unpacking values in a collection of list to var
cars = ['Toyota', 'Nissan', 'Marcedes']
c1, c2, c3 = cars
print(c1)
print(c2)
print(c3)
