# Python - Modify Strings

# Upper case
str1 = "string lower case to upper case"
print(str1.upper())

# Lower case
str2 = "STRING UPPER CASE TO LOWER CASE"
print(str2.lower())

# Removing whitespace
str3 = "   Removing whitespaces   "
print(str3.strip())

# Replacing string
str4 = "take"
print(str4.replace("t", "m"))
print(str4.replace("take", "lake"))

# Spliting a string
str5 = "Hello, World"
print(str5.split(","))
